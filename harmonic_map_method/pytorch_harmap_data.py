def thr(p):
    x = p[...,0]
    y=  p[...,1]
    area = np.array([0.51,1,0.51,1,-0.4,0.4])
    in_x = (x >= area[0]) & (x <= area[1])
    in_y = (y >= area[2]) & (y <= area[3])
    if p.shape[-1] == 3:
        z = p[...,2]
        in_z = (z >= area[4]) & (z <= area[5])
        return in_x & in_y & in_z
    return  in_x & in_y

